package tw.kaiyeee.triptao.app;

public class Broadcast {
    //類型
    private String name;
    //項目
    private String time;

    //P.S. 以上變數名稱與串接欄位必需相符

    public Broadcast()
    {

    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getName()
    {
        return name;
    }

    public void setTime(String time)
    {
        this.time = time;
    }

    public String getTime()
    {
        return time;
    }
}
